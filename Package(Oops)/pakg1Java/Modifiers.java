//package pakg1Java;

public class Modifiers {
    // public

    public int pub = 10;
    // protected
    protected int prot = 20;
    // default 
    int deflt = 30;
    // private
    private int priv = 40;

    public void show() {
        System.out.println("Public: " + pub);
        System.out.println("Protected: " + prot);
        System.out.println("Default: " + deflt);
        System.out.println("Private: " + priv);
    }


}
